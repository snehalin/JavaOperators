
public class Bitwise {

	public static void main(String[] args) {
		int a=10;//
		int b=20;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		a=10;
		System.out.println(~a);
		
		//1010 ->0101
		
		//          32 16 8 4 2 1
		//           0  0 1 0 1 0      10
	    //           0  1 0 1 0 0      20
        //           0  1 1 1 1 0      30
		}
}